package com.gcmb.hsbc.model;

public class Rates{

	private float GBP;
	
	private float USD;
	
	private float HKD;

	public float getGBP() {
		return GBP;
	}

	public void setGBP(float gBP) {
		GBP = gBP;
	}

	public float getUSD() {
		return USD;
	}

	public void setUSD(float uSD) {
		USD = uSD;
	}

	public float getHKD() {
		return HKD;
	}

	public void setHKD(float hKD) {
		HKD = hKD;
	}

	
}
